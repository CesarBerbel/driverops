class Command(BaseCommand):
    help = (
        "Popula o catálogo com os 100 serviços mais realizados em oficinas "
        "(peças padrão obrigatórias/opcionais) e combos. Não cria cliente, "
        "fornecedor nem OS."
    )

    @transaction.atomic
    def handle(self, *args, **options):
        self._validate()
        cats = self._seed_categories()
        parts = self._seed_parts(cats)
        services = self._seed_services(cats, parts)
        packages = self._seed_packages(services)

        links = ServicePart.objects.filter(service__in=services.values()).count()
        self.stdout.write(
            self.style.SUCCESS(
                "Catálogo populado: "
                f"{len(PART_CATEGORIES) + len(SERVICE_CATEGORIES)} categorias, "
                f"{len(parts)} peças, {len(services)} serviços "
                f"({links} vínculos peça↔serviço) e {len(packages)} combos. "
                "Nenhum cliente, fornecedor ou OS foi criado."
            )
        )

    def _validate(self):
        """Falha cedo se os dados estiverem inconsistentes (código de peça
        inexistente, categoria desconhecida, nome de serviço num combo que não
        existe, ou número de serviços diferente de 100)."""
        if len(SERVICES) != 100:
            raise CommandError(f"Esperados 100 serviços, encontrados {len(SERVICES)}.")

        part_codes = {code for code, *_ in PARTS}
        if len(part_codes) != len(PARTS):
            raise CommandError("Há códigos de peça duplicados em PARTS.")
        part_cats = set(PART_CATEGORIES)
        for code, _n, _b, cat, *_ in PARTS:
            if cat not in part_cats:
                raise CommandError(f"Peça {code}: categoria de peça inválida {cat!r}.")

        service_cats = set(SERVICE_CATEGORIES)
        service_names = set()
        for name, cat, _d, _l, _m, std in SERVICES:
            if name in service_names:
                raise CommandError(f"Serviço duplicado: {name!r}.")
            service_names.add(name)
            if cat not in service_cats:
                raise CommandError(f"Serviço {name!r}: categoria inválida {cat!r}.")
            for code, _qty, _req in std:
                if code not in part_codes:
                    raise CommandError(
                        f"Serviço {name!r} referencia peça inexistente {code!r}."
                    )

        for name, _d, _dt, _dv, svc_names in PACKAGES:
            for svc in svc_names:
                if svc not in service_names:
                    raise CommandError(
                        f"Combo {name!r} referencia serviço inexistente {svc!r}."
                    )

    def _seed_categories(self):
        cats = {"part": {}, "service": {}}
        for name in PART_CATEGORIES:
            cats["part"][name] = first_or_create(
                Category, {"category_type": "part", "name": name}
            )
        for name in SERVICE_CATEGORIES:
            cats["service"][name] = first_or_create(
                Category, {"category_type": "service", "name": name}
            )
        return cats

    def _seed_parts(self, cats):
        parts = {}
        for code, name, brand, cat, unit, cost, sale, cur, minimum in PARTS:
            obj = first_or_create(
                Part,
                {"internal_code": code},
                {
                    "name": name,
                    "brand": brand,
                    "category": cats["part"][cat],
                    "unit_of_measure": unit,
                    "cost_price": money(cost),
                    "sale_price": money(sale),
                    "current_quantity": money(cur),
                    "min_quantity": money(minimum),
                    "is_active": True,
                },
            )
            parts[code] = obj
        return parts

    def _seed_services(self, cats, parts):
        services = {}
        for name, cat, desc, labor, minutes, std in SERVICES:
            obj = first_or_create(
                Service,
                {"name": name},
                {
                    "category": cats["service"][cat],
                    "description": desc,
                    "labor_cost": money(labor),
                    "estimated_minutes": minutes,
                    "is_active": True,
                },
            )
            # Reconstrói as peças padrão para refletir estes dados a cada execução.
            obj.standard_parts.all().delete()
            ServicePart.objects.bulk_create(
                ServicePart(
                    service=obj,
                    part=parts[code],
                    suggested_quantity=money(qty),
                    is_required=required,
                )
                for code, qty, required in std
            )
            services[name] = obj
        return services

    def _seed_packages(self, services):
        packages = {}
        for name, desc, discount_type, discount_value, service_names in PACKAGES:
            obj = first_or_create(
                ServicePackage,
                {"name": name},
                {
                    "description": desc,
                    "discount_type": discount_type,
                    "discount_value": money(discount_value),
                    "is_active": True,
                },
            )
            # Mantém desconto/descrição em dia mesmo se o combo já existia.
            obj.description = desc
            obj.discount_type = discount_type
            obj.discount_value = money(discount_value)
            obj.is_active = True
            obj.save(
                update_fields=[
                    "description",
                    "discount_type",
                    "discount_value",
                    "is_active",
                ]
            )
            obj.items.all().delete()
            PackageService.objects.bulk_create(
                PackageService(package=obj, service=services[n]) for n in service_names
            )
            packages[name] = obj
        return packages
