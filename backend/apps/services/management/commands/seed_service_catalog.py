def first_or_create(model, lookup, defaults=None):
    """Como get_or_create, mas tolerante a duplicatas pré-existentes num banco
    de desenvolvimento (ex.: duas categorias com o mesmo nome) -- devolve a
    primeira que casar."""
    obj = model.objects.filter(**lookup).first()
    if obj is None:
        obj = model.objects.create(**{**lookup, **(defaults or {})})
    return obj


